export async function getServerSideProps(context) {

  const { userId, token } = await redirectUser(context);

  if (!userId) {
    return {
      props: {},
      redirect: {
        destination: "/login",
        permanent: false,
      },
    };
  }
  


  const watchItAgainVideos = await getWatchItAgainVideos(userId, token);

  const ACTION_ADVANTURE =  await getVideos("Action advanture Games PS4 Trailer");
  const FPS =  await getVideos("fps Games PS4 Trailer");
  const RPG =  await getVideos("rpg Games PS4 Trailer");
  const JRPG =  await getVideos("Jrpg Games PS4 Trailer");



const popularVideos = await getPopularVideos();





  return {
    props: {ACTION_ADVANTURE,FPS,RPG,JRPG,popularVideos, watchItAgainVideos,}, // will be passed to the page component as props
  };
}

